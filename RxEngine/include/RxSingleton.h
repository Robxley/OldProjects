#pragma once


#ifndef _RXSINGLETON_H_
#define _RXSINGLETON_H_




template <class T>
class RxSingleton
{
public :
 
    //----------------------------------------------------------------
    // Renvoie l'instance unique de la classe
    //----------------------------------------------------------------
    static T& Instance()
    {
        static T Inst;
        return Inst;
    }
 

protected :
 
    //----------------------------------------------------------------
    // Constructeur par défaut
    //----------------------------------------------------------------
    RxSingleton() {}
 
    //----------------------------------------------------------------
    // Destructeur
    //----------------------------------------------------------------
    ~RxSingleton() {}
 
private :
 
    //----------------------------------------------------------------
    // Copie interdite
    //----------------------------------------------------------------
    RxSingleton(RxSingleton&);
    void operator =(RxSingleton&);
};
 
//----------------------------------------------------------------
// Déclaration de notre variable statique
//----------------------------------------------------------------


#endif